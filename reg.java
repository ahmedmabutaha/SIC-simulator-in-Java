
public class reg {
    
    private int pc=000000;
    private int Ac;
    int x=000000;
    private int l=000000;
    private char sw='0';

    public int getPc() {
        return pc;
    }

    public int getAc() {
        return Ac;
    }

    public int getX() {
        return x;
    }

    public int getL() {
        return l;
    }

    public int getSw() {
        return sw;
    }

    public void setPc(int pc) {
        this.pc = pc;
    }

    public void setAc(int Ac) {
        this.Ac = Ac;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setL(int l) {
        this.l = l;
    }

    public void setSw(char sw) {
        this.sw = sw;
    }


    
    
}
